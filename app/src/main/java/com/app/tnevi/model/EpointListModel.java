package com.app.tnevi.model;

public class EpointListModel {

    private String id, epoint, amount;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEpoint() {
        return epoint;
    }

    public void setEpoint(String epoint) {
        this.epoint = epoint;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }
}
